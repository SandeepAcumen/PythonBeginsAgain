#5. Big-O Notation? (O stands for order-of)
'''
Big-O notation describes how fast (or how slow) an algorithm grows 
as the input size increases.

Big-O	   Name	            Description	                                              Example
O(1)	  Constant time	    Takes the same time regardless of input size	         Accessing arr[5]
O(log n)  Logarithmic	    Time grows slowly as input grows	                     Binary search
O(n)	  Linear	        Grows directly with input	                             Simple for loop
O(n log n)Linearithmic	    Common in efficient sorts	                             Merge sort, Quick sort
O(n²)	  Quadratic	        Time grows quickly (nested loops)	                     Bubble sort, Selection sort
O(2ⁿ)	  Exponential	    Doubles with each input increase	                     Recursive Fibonacci
O(n!)	  Factorial	        Extremely slow	                                         Permutation generation
'''

